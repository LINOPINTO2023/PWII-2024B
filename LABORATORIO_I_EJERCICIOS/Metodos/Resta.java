package Metodos;

/**
 * Método para restar dos números.
 * 
 * @param num1 El primer número.
 * @param num2 El segundo número.
 * @return El resultado de restar ambos números. Devuelve double.
 */
public class Resta {
    public static double restar(double num1, double num2) {
        return num1 - num2;
    }
}
