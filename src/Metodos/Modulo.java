package Metodos;

/**
 * Método para calcular el módulo de dos números.
 * 
 * @param num1 El primer número.
 * @param num2 El segundo número.
 * @return El resultado del módulo de num1 entre num2. Devuelve double.
 */
public class Modulo {
    public static double modulo(double num1, double num2) {
        return num1 % num2;
    }
}
