package Metodos;

/**
 * Método para sumar dos números.
 * 
 * @param num1 El primer número.
 * @param num2 El segundo número.
 * @return El resultado de sumar ambos números. Devuelve double.
 */
public class Suma {
    public static double sumar(double num1, double num2) {
        return num1 + num2;
    }
}
